export type ProductDataType = {
  [key in MaterialEnum]: {
    modes: ModesEnum[],
    group: MaterialGroupsEnum,
    key: string,
    buildings: Partial<Record<ModesEnum, BuildingsEnum[]>>,
    imageRow: number,
    imageCol: number,
    displayName: Record<string, string>
  }
}

export type buildingDataType = {
  [key in BuildingsEnum]: {
    modes: ModesEnum,
    group: BuildingGroupEnum,
    power: number,

    input?: {
      name: MaterialEnum,
      perSec: number,
    }[]
    output: Partial<Record<ModesEnum, {
      name: MaterialEnum,
      perSec: number,
      unitInput?: MaterialEnum,
      unitCost?: {
        name: MaterialEnum,
        perSec: number
      }[]
    }[]>>

    booster?: {
      name: MaterialEnum,
      perSec: number
      speedBoost: number
    }[]

    affinities?: ({ type: FloorsEnum | WallsEnum } &
    ({
      efficiency: number,
      affinity?: number
    } | {
      efficiency?: number,
      affinity: number
    }))[]

    speedIncrease?: number,

    imageRow: number,
    imageCol: number,
    displayName: Record<string, string>
  }
}

